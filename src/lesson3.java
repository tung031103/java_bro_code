public class lesson3 {
    public static void main(String[] args) {
        // swap 2 variable in java
        String x = "water";
        String y = "Kool-Aid";
        String temp;
        temp = x;
        x = y;
        y = temp;
        System.out.println("x: " + x);
        System.out.println("y: " + y);
    }
}
