package lesson26;

public class pizza {

    String bread;
    String sauce;
    String cheese;
    String topping;

    pizza() {

    }

    pizza(String bread) {
        this.bread = bread;
    }

    pizza(String bread, String sauce) {
        this.bread = bread;
        this.sauce = sauce;

    }

    pizza(String bread, String sauce, String cheese) {
        this.bread = bread;
        this.sauce = sauce;
        this.cheese = cheese;
    }

    pizza(String bread, String sauce, String cheese, String topping) {
        this.bread = bread;
        this.sauce = sauce;
        this.cheese = cheese;
        this.topping = topping;
    }
}
